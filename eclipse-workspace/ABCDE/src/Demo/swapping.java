package Demo;

public class swapping {
	
	public static void main(String[] args) {
		
		int a = 21;
		int b = 23;
		int c;
		
		c = a;
		a = b;
		b = c;
		
		System.out.print("swapping a = ");
		System.out.println(a);
		System.out.print("swapping b = ");
		System.out.println(b);
		System.out.print("swapping c = ");
		System.out.println(c);
		
	}

}
